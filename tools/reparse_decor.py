for name, body in methods:
    if name.islower():
        rust_name = f"DECOR_METHOD_LOWER_{name.upper()}"
    else:
        rust_name = f"DECOR_METHOD_UPPER_{name}"
        
    statements = body.strip().split(';')
    segments = []
    for stmt in statements:
        stmt = stmt.strip()
        if not stmt or 'this.a.a.a' not in stmt:
            continue
            
        p_idx_match = re.search(r'this\.a\.p\[(\d+)\]', stmt)
        raw_sprite_match = re.search(r'this\.a\.a\.a\(\s*this\.a\.a\(\),\s*(\d+),', stmt)
        
        if p_idx_match:
            p_idx = int(p_idx_match.group(1))
            sprite_id = decor_palette[p_idx]
        elif raw_sprite_match:
            sprite_id = int(raw_sprite_match.group(1))
        else:
            continue
            
        # Parse X offset
        x_part = stmt.split(',')[2].strip()
        x_offset = 0.0
        x_tail = re.search(r'this\.a\.M\s*/\s*2\s*([+-]\s*\d+)?$', x_part)
        if x_tail and x_tail.group(1):
            x_offset = float(x_tail.group(1).replace(' ', ''))
        elif not x_tail:
            tail = re.search(r'([+-]\s*\d+)\s*$', x_part)
            if tail:
                x_offset = float(tail.group(1).replace(' ', ''))
                
        # Parse Y offset
        y_part = stmt.split(',')[3].strip()
        if y_part.endswith(')'):
            y_part = y_part[:-1]
        y_offset = 0.0
        y_tail = re.search(r'this\.a\.M\s*/\s*2\s*([+-]\s*\d+)?$', y_part)
        if y_tail and y_tail.group(1):
            y_offset = float(y_tail.group(1).replace(' ', ''))
        elif not y_tail:
            tail = re.search(r'([+-]\s*\d+)\s*$', y_part)
            if tail:
                y_offset = float(tail.group(1).replace(' ', ''))
                
        segments.append((sprite_id, x_offset, y_offset))
        
    output_lines.append(f"pub const {rust_name}: &[DecorSegment] = &[")
    for sprite_id, dx, dy in segments:
        output_lines.append(f"    DecorSegment {{ sprite_id: {sprite_id}, dx: {dx}f32, dy: {dy}f32 }},")
    output_lines.append("];")
    output_lines.append("")

