with open('/home/cherry/Downloads/Robinson/decompiled/h.java', 'r') as f:
    content = f.read()

method_pattern = re.compile(
    r'public\s+final\s+void\s+(\w+)\(int\s+n2,\s+int\s+n3\)\s*\{([^}]+)\}',
    re.MULTILINE
)

methods = method_pattern.findall(content)

output_lines = []
output_lines.append("// Generated by tools/reparse_decor.py")
